int	mouse_handler(int button, int x, int y, t_fractal *fractal)
{
	if (!fractal) // Verifica si fractal es NULL
		return (0);
	if (button == Button5)
	{
		fractal->zoom *= 0.95;
	}
	else if (button == Button4)
	{
		fractal->zoom *= 1.05;
	}
	fractal_render(fractal);
	x = x + y;
	return (0);
}
/*
Teclas para hacer zoom, Button5 y 4 son la ruedecilla del mouse,
el resto son para moverte o para cambiar los colores
*/

int	julia_track(int x, int y, t_fractal *fractal)
{
	if (!ft_strncmp(fractal->name, "julia", 5))
	{
		fractal->julia_x = (map(x, -2, +2, WIDTH) * fractal->zoom)
			+ fractal->shift_x;
		fractal->julia_y = (map(y, +2, -2, HEIGHT) * fractal->zoom)
			+ fractal->shift_y;
		fractal_render(fractal);
	}
	return (0);
}
